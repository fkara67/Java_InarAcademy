package chapters.chapter_04;

public class CheckPoint04_12 {

	public static void main(String[] args) {
		char c = 'A';
		int i = (int)c;
		System.out.println(i);
		
		float f = 1000.34f;
		int j = (int)f;
		System.out.println(j);
		
		double d = 1000.34;
		int k = (int)d;
		System.out.println(k);
		
		int s = 97;
		char z = (char)s;
		System.out.println(z);
		
	}

}
